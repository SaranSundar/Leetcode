from typing import List
from collections import deque


class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        islands = 0
        for r in range(len(grid)):
            for c in range(len(grid[r])):
                if grid[r][c] == '1':
                    islands += 1
                    self.sinkIsland(grid, r, c)

        return islands

    def sinkIsland(self, grid: List[List[str]], r, c):
        queue = deque([(r, c)])
        while queue:
            cell = queue.popleft()

            directions = [(1, 0), (-1, 0), (0, 1), (0, -1)]
            for direction in directions:
                newCell = (direction[0] + cell[0], direction[1] + cell[1])
                if 0 <= newCell[0] < len(grid) and 0 <= newCell[1] < len(grid[newCell[0]]):
                    if grid[newCell[0]][newCell[1]] == '1':
                        queue.append(newCell)
                        grid[newCell[0]][newCell[1]] = '0'


